def remove_duplicates(nums):
    if not nums:
        return 0
    
    # Ініціалізуємо перший вказівник
    i = 0

    # Проходимо по масиву починаючи з другого елемента
    for j in range(1, len(nums)):
        # Якщо знайдено новий унікальний елемент
        if nums[j] != nums[i]:
            i += 1  # Зсув вказівника для нового унікального елемента
            nums[i] = nums[j]  # Записуємо унікальний елемент на правильну позицію

    # Кількість унікальних елементів дорівнює i + 1
    return i + 1

# Приклад використання
nums1 = [1, 1, 2]
k1 = remove_duplicates(nums1)
print(k1, nums1[:k1])  # Виведе: 2, [1, 2]

nums2 = [0, 0, 1, 1, 1, 2, 2, 3, 3, 4]
k2 = remove_duplicates(nums2)
print(k2, nums2[:k2])  # Виведе: 5, [0, 1, 2, 3, 4]
